const getDateLocale = (lang: AdminLanguage) => {
  if (lang === "en") return enUS;
  if (lang === "it") return it;
  return es;
};

export const AdminI18nProvider = ({ children }: { children: React.ReactNode }) => {
  const location = useLocation();
  const [lang, setLang] = useState<AdminLanguage>("es");

  useEffect(() => {
    if (location.pathname.startsWith("/en/")) {
      setLang("en");
      return;
    }
    if (location.pathname.startsWith("/it/")) {
      setLang("it");
      return;
    }
    setLang("es");
  }, [location.pathname]);

  useEffect(() => {
    localStorage.setItem(STORAGE_KEY, lang);
  }, [lang]);

  const value = useMemo<AdminI18nContextValue>(() => {
    const t = (key: string, vars?: Record<string, string | number>) => {
      const entry = translations[lang][key] ?? translations.es[key] ?? key;
      if (!vars) return entry;
      return Object.keys(vars).reduce(
        (acc, varKey) =>
          acc.replace(new RegExp(`\\{${varKey}\\}`, "g"), String(vars[varKey])),
        entry
      );
    };

    const pathPrefix = lang === "es" ? "" : `/${lang}`;

    return {
      lang,
      setLang,
      t,
      dateLocale: getDateLocale(lang),
      pathPrefix,
    };
  }, [lang]);

  return <AdminI18nContext.Provider value={value}>{children}</AdminI18nContext.Provider>;
};

export const useAdminI18n = () => {
  const ctx = useContext(AdminI18nContext);
  if (!ctx) {
    throw new Error("useAdminI18n must be used within AdminI18nProvider");
  }
  return ctx;
};
